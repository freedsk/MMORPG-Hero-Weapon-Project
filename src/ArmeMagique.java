

public class ArmeMagique {
    private String nom;
    private int degatsMagiques;

    public ArmeMagique(String nom, int degatsMagiques) {
        this.nom = nom;
        this.degatsMagiques = degatsMagiques;
    }

    public String getNom() { return nom; }
    public int getDegatsMagiques() { return degatsMagiques; }
}
